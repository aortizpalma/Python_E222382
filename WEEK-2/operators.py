'''- Operators
    - Assignment
    - Arithmetic
    - Comparison
    - Logical   '''

# Arithmetic operators
print(1 + 2)
print(1 - 2)
print(2 * 3)
print(3 / 2)
print(3 % 2)
print(3 // 2)
print(2 ** 3)


# Assigment operators
print(10)
a = 100
print(a)

a += 10
print(a)

a -= 10
print(a)

# Comparison operators

print(3 > 2)
print(2 < 3)
print(2 <= 3)
print(3 >= 2)
print(3 <= 2)
print(3 == 3)
print(3 == '3')
print(3 == int('3'))
print(str(3) == '3')


# Logical operators
print(3 > 2 and 2 > 1)
print(3 > 2 and 2 < 1)
print(3 > 2 or 1 < 0)
print(3 < 2 or 1 < 0)
print(not False)
print(not not False)
print(not 4 > 5)
print(not 'land' in 'Finland')
